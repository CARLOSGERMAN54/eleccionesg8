    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int columna = 0;
        int fila = this.tablaVotantes.getSelectedRow();
        String id = this.tablaVotantes.getValueAt(fila, columna).toString();
        ClsResultado mensaje = this.ctlVotante.eliminarVotante(id);
        if (mensaje.getTipo().equals(ClsResultado.OK)) {
            ObtenerVotantes();
        }
        mensaje.mostrarMensaje();
    }//GEN-LAST:event_btnEliminarActionPerformed

     public void ObtenerVotantes() {
            this.listavotantes = this.ctlVotante.ObtenerVotantes();
            System.out.print(this.listavotantes);
            this.actualizarTabla(this.listavotantes);
    }
    
    private void actualizarTabla(LinkedList<ClsVotante> votantes) {

        DefaultTableModel modelo = (DefaultTableModel) this.tablaVotantes.getModel();
        modelo.setRowCount(0);

        for (ClsVotante c : votantes) {

            Object[] fila = {c.identificacion, c.nombres +" "+ c.apellidos, c.getTelefono(),
                c.getCorreo(),c.direccionVotacion,c.mesaVotacion};

            modelo.addRow(fila);
        }
    }
    
  public ClsVotante buscarVotante(String idVotante) {
        for (ClsVotante c : this.listavotantes) {
            if (idVotante.equals(c.getIdentificacion())) {
                return c;
            }
        }

        return null;

    }
    
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        this.panelPestanias.setSelectedIndex(1);
        int columna = 0;
        int fila = this.tablaVotantes.getSelectedRow();
        String id = this.tablaVotantes.getValueAt(fila, columna).toString();
        ClsVotante votante = this.buscarVotante(id);
        //System.out.println(candidato.identificacion);
        if (votante != null) {
            this.identificacion.setEnabled(false);
            this.btnAgregar.setVisible(false);
            this.btnEditar.setVisible(true);
            this.btnNuevo.setVisible(true);
            this.identificacion.setText(votante.getIdentificacion());
            this.tipoDocumento.setSelectedItem(votante.getTipoIdentificacion());
            this.nombres.setText(votante.getNombres());
            this.apellidos.setText(votante.getApellidos());
            this.telefono.setText(votante.getTelefono());
            this.correo.setText(votante.getCorreo());
            this.direccion.setText(votante.getDireccion());
        }
        this.panelPestanias.setSelectedIndex(0);
        this.btnActualizar.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

