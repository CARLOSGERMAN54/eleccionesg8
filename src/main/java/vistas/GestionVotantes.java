    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here: 
        String identificacion = this.identificacion.getText();
        String tipoIdentificacion = (String)this.tipoDocumento.getSelectedItem();
        String nombres = this.nombres.getText();
        String apellidos = this.apellidos.getText();
        String telefono = this.telefono.getText();
        String correo = this.correo.getText();
        String direccionVotacion = "";
        String mesaVotacion = "";
        if(this.clsValidacion.esCadenaNumerica(identificacion)){
            if(this.clsValidacion.validarNombresPersonas(nombres) && this.clsValidacion.validarNombresPersonas(apellidos)){
                if(this.clsValidacion.esCadenaNumerica(telefono)){
                    if(this.clsValidacion.validarCorreo(correo)){
                        if(tipoIdentificacion=="CC"){
                            ClsVotante votante = new ClsVotante(direccionVotacion,mesaVotacion,1,identificacion,tipoIdentificacion,nombres,apellidos,correo,telefono,direccionVotacion);
                            ClsResultado res = this.ctlVotante.adicionarVotante(votante);
                            res.mostrarMensaje();
                            if(res.getTipo() == ClsResultado.OK)
                            {
                                this.limpiarFormulario();
                            }
                        }
                       else
                        {
                          JOptionPane.showMessageDialog(null, "El tipo de documento debe ser cédula (CC)");  
                        }

                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Correo invalido");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Número telefóncio invalido");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Los Nombres y Apellidos deben contener caracteres alfabéticos ");
            }
        }
        else
        {
          JOptionPane.showMessageDialog(null, "Cédula inválida");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

